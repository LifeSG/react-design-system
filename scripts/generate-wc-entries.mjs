/**
 * generate-wc-entries.mjs
 *
 * Scans src/ component directories, discovers primary PascalCase exports,
 * and generates a single src/web-components.ts entry file that registers
 * all components as custom elements via react-to-webcomponent.
 *
 * Components that need special event handling (Button, ModalV2) get inline
 * wrapper functions. All others get flat registrations.
 *
 * Usage:
 *   node scripts/generate-wc-entries.mjs
 */
import fs from "node:fs";
import path from "node:path";

const rootDir = process.cwd();
const srcDir = path.join(rootDir, "src");
const outputFile = path.join(srcDir, "web-components.ts");

// Directories to skip — not user-facing components
const SKIP_DIRS = new Set([
    "shared",
    "util",
    "__mocks__",
    "theme",
    "animations",
    "layout",
    "form",
    "local-nav",
    "unit-number",
    "text-list",
    "typography",
    "button", // has dedicated web-component.ts wrapper
    "modal-v2", // has dedicated web-component.ts wrapper
]);

// Directories with dedicated web-component.ts wrapper files
const CUSTOM_WRAPPER_DIRS = ["button", "modal-v2"];

// =============================================================================
// Helpers
// =============================================================================

function toPascalCase(dirName) {
    return dirName
        .split("-")
        .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
        .join("");
}

function toKebabCase(pascal) {
    return pascal
        .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
        .replace(/([A-Z])([A-Z][a-z])/g, "$1-$2")
        .toLowerCase();
}

function findPrimaryExport(dirPath, dirName) {
    const expected = toPascalCase(dirName);
    const files = fs
        .readdirSync(dirPath)
        .filter(
            (f) =>
                f.endsWith(".tsx") &&
                !f.includes(".test.") &&
                !f.includes(".stories.")
        );

    for (const file of files) {
        const content = fs.readFileSync(path.join(dirPath, file), "utf8");
        const regex = /export const ([A-Z][A-Za-z0-9]*)/g;
        let match;
        while ((match = regex.exec(content)) !== null) {
            if (match[1] === expected) return expected;
        }
    }
    return null;
}

// =============================================================================
// Main
// =============================================================================

const componentDirs = fs
    .readdirSync(srcDir, { withFileTypes: true })
    .filter(
        (d) =>
            d.isDirectory() &&
            !SKIP_DIRS.has(d.name) &&
            fs.existsSync(path.join(srcDir, d.name, "index.ts"))
    )
    .map((d) => d.name)
    .sort();

const entries = [];
for (const dirName of componentDirs) {
    const dirPath = path.join(srcDir, dirName);
    const exportName = findPrimaryExport(dirPath, dirName);
    if (!exportName) {
        console.warn(
            `  Skipped: ${dirName} (no primary export "${toPascalCase(
                dirName
            )}" found)`
        );
        continue;
    }
    const tag = `lifesg-${dirName}`;
    entries.push({ dirName, exportName, tag });
}

// Auto-discovered imports
const autoImports = entries
    .map((e) => `import { ${e.exportName} } from "./${e.dirName}";`)
    .join("\n");

// Auto-discovered registrations (flat, no props config)
const autoRegistrations = entries
    .map(
        (e) =>
            `if (!customElements.get("${e.tag}")) {\n    customElements.define("${e.tag}", r2wc(wrap(${e.exportName}), React, ReactDOM));\n}`
    )
    .join("\n");

// =============================================================================
// Generate output
// =============================================================================

const customWrapperImports = CUSTOM_WRAPPER_DIRS.map(
    (dir) => `import "./${dir}/web-component";`
).join("\n");

const content = `// AUTO-GENERATED by scripts/generate-wc-entries.mjs
// Do not edit this file directly.
import React from "react";
import ReactDOM from "react-dom";
import r2wc from "react-to-webcomponent";

import "./theme/styles/lifesg.css";

// Custom wrappers (with event handling / compound composition)
${customWrapperImports}

// Auto-registered components
${autoImports}

// Strip r2wc's internal \`container\` prop so it doesn't leak to the DOM
function wrap(Component: React.ComponentType<any>) {
    return ({ container, ...props }: any) => React.createElement(Component, props);
}

${autoRegistrations}

export {};
`;

// Write
const existing = fs.existsSync(outputFile)
    ? fs.readFileSync(outputFile, "utf8")
    : undefined;

if (existing !== content) {
    fs.writeFileSync(outputFile, content);
    console.log(`Generated: ${outputFile}`);
} else {
    console.log("No changes needed.");
}

console.log(
    `Registered ${entries.length + 2} components as web components (${
        entries.length
    } auto + 2 custom).`
);
