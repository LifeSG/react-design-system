// Vue Components Build Script
//
// Generates a wrapper entry that exposes React components as Vue components
// via Veaury's applyReactInVue(). The entry file is generated before rollup
// and cleaned up afterwards — it never persists in the repo.
//
// Usage:
//   node scripts/vue-components.mjs generate
//   node scripts/vue-components.mjs clean
import fs from "node:fs";
import path from "node:path";

const rootDir = process.cwd();
const srcDir = path.join(rootDir, "src");

const ENTRY_MARKER = "AUTO-GENERATED by scripts/vue-components.mjs";

// =============================================================================
// Component definitions
// =============================================================================

/**
 * Each entry describes one Vue-wrapped component to export.
 *   reactImport:  the named export from ./index
 *   vueExport:    the exported Vue component name
 *   subComponent: if true, this is a compound sub-component (e.g. ModalV2.Card)
 *   parentImport: for sub-components, the parent's named export
 *   memberName:   for sub-components, the property name on the parent
 */
const components = [
    {
        reactImport: "Button",
        vueExport: "LsButton",
    },
    {
        reactImport: "ModalV2",
        vueExport: "LsModal",
    },
    {
        vueExport: "LsModalCard",
        subComponent: true,
        parentImport: "ModalV2",
        memberName: "Card",
    },
    {
        vueExport: "LsModalCloseButton",
        subComponent: true,
        parentImport: "ModalV2",
        memberName: "CloseButton",
    },
    {
        vueExport: "LsModalContent",
        subComponent: true,
        parentImport: "ModalV2",
        memberName: "Content",
    },
    {
        vueExport: "LsModalFooter",
        subComponent: true,
        parentImport: "ModalV2",
        memberName: "Footer",
    },
];

// =============================================================================
// Generate
// =============================================================================

function generate() {
    // Collect unique top-level React imports
    const reactImports = new Set();
    for (const comp of components) {
        if (comp.subComponent) {
            reactImports.add(comp.parentImport);
        } else {
            reactImports.add(comp.reactImport);
        }
    }

    const lines = [
        `// ${ENTRY_MARKER}`,
        "// Do not edit this file directly.",
        `import { applyReactInVue } from "veaury";`,
        `import { ${[...reactImports].sort().join(", ")} } from "./index";`,
        "",
    ];

    for (const comp of components) {
        if (comp.subComponent) {
            lines.push(
                `export const ${comp.vueExport} = applyReactInVue(${comp.parentImport}.${comp.memberName});`
            );
        } else {
            lines.push(
                `export const ${comp.vueExport} = applyReactInVue(${comp.reactImport});`
            );
        }
    }

    lines.push("");

    const entryPath = path.join(srcDir, "vue-components.ts");
    fs.writeFileSync(entryPath, lines.join("\n"));
    console.log("  Generated: src/vue-components.ts");
}

// =============================================================================
// Clean
// =============================================================================

function clean() {
    const entryPath = path.join(srcDir, "vue-components.ts");
    if (fs.existsSync(entryPath)) {
        const content = fs.readFileSync(entryPath, "utf8");
        if (content.includes(ENTRY_MARKER)) {
            fs.rmSync(entryPath);
            console.log("  Cleaned: src/vue-components.ts");
        }
    }
}

// =============================================================================
// CLI
// =============================================================================

const command = process.argv[2];

if (command === "generate") {
    console.log("Generating Vue component wrappers...");
    generate();
    console.log("Done.");
} else if (command === "clean") {
    console.log("Cleaning generated Vue component files...");
    clean();
    console.log("Done.");
} else {
    console.error(`Usage: node scripts/vue-components.mjs <generate|clean>`);
    process.exit(1);
}
